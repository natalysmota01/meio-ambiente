				<h3>O que é Mudanças Climáticas?</h3>
				<p>O Clima pode ser definido como o conjunto de condições meteorológicas (temperatura, umidade, chuvas, pressão e ventos) que mantém características comuns em uma determinada região. Variações no clima fazem parte da dinâmica ambiental do planeta. Por exemplo, a diferença das características de uma mesma estação de um ano para outro, que pode ser mais quente ou fria, úmida ou seca, chuvosa ou não. Também são evidências das variações do clima os fenômenos como tempestades, ciclones e secas.</p>

				<p>As mudanças climáticas são uma alteração permanente nessas características e aconteceram diversas vezes no passado, por causas naturais. Entretanto, as atividades humanas, em especial as que utilizam combustíveis fósseis, vêm influenciando a ocorrência desse tipo de evento, por meio da alteração do equilíbrio climático do planeta. A causa central deste fenômeno é a intensificação do efeito estufa, que modifica o modo com que a energia solar interage com a atmosfera, provocando graves conseqüências.</p>

				<p>Alguns indicadores das mudanças climáticas nos últimos 15 anos são o aquecimento global, alterações bruscas em características básicas das estações do ano em diferentes partes do planeta, como temperatura e ocorrência de chuvas, ou aumento inédito nas últimas décadas de fenômenos abruptos como vendavais, ciclones e enchentes. <span>Fonte: http://bioclimatico.com.br</span></p>

				<p>As mudanças climáticas antropogênicas, ou seja, aquelas causadas pelo homem, estão associadas ao aumento da emissão de gases de efeito estufa por queima de combustíveis fósseis (dos automóveis, das indústrias, usinas termoelétricas), queimadas, desmatamento,
				decomposição de lixo etc. A partir do final do século 18 (Revolução Industrial) e na segunda metade do século 20, houve uma expansão da produção industrial, o que gerou um grande aumento de emissões de gases de efeito estufa na atmosfera. Existem fortes indícios de que o clima está de fato mudando. As décadas de 1990 e 2000 foram as mais quentes dos últimos 1.000 anos. As projeções do Painel Intergovernamental de Mudanças Climáticas (IPCC) indicam que nos próximos 100 anos poderá haver um aumento da temperatura média global entre 1,8°C e 4,0°C, e um aumento do nível médio do mar entre 0,18 m e 0,59 m, o que poderá afetar significativamente as atividades humanas e os ecossistemas terrestres. <span>Fonte: http://inpe.br</span></p>

